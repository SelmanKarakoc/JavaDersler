package day11_ForLoops;

public class C12_MethodCreation {
    public static void main(String[] args) {
        //5 in faktoriyelini hesapla
        int input=5;
        int carpim=1;
        for (int i = input; i >=1 ; i--) {
            carpim*=i;


        }
        System.out.println("verilen "+ input + " sayısının faktoriyeli: "+ carpim);


















    }
}
